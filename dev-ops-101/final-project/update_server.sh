#!/bin/bash

# Скрипт для автоматического обновления и апгрейда пакетов на Linux-сервере

# Обновляем список доступных пакетов
echo "Обновление списка пакетов..."
sudo apt update -y

# Выполняем обновление пакетов
echo "Обновление установленных пакетов..."
sudo apt upgrade -y

# Выполняем полное обновление системы
echo "Выполнение полного апгрейда..."
sudo apt full-upgrade -y

# Удаляем устаревшие и ненужные пакеты
echo "Очистка системы от ненужных пакетов..."
sudo apt autoremove -y
sudo apt autoclean -y

echo "Обновление и апгрейд завершены успешно!"
